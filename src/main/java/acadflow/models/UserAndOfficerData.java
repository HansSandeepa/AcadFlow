package acadflow.models;

public class UserAndOfficerData {

        public String name, dob, gender, email, dept, officerId, hireDate;

        public UserAndOfficerData(String name, String dob, String gender, String email, String dept, String officerId, String hireDate) {
            this.name = name;
            this.dob = dob;
            this.gender = gender;
            this.email = email;
            this.dept = dept;
            this.officerId = officerId;
            this.hireDate = hireDate;
        }

}
